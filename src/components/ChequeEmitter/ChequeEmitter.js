const ChequeEmitter = () => {
  const [web3, setWeb3] = useState(null);
  const [account, setAccount] = useState('');
  const [nftChequeContract, setNftChequeContract] = useState(null);
  const [recipient, setRecipient] = useState('');
  const [amount, setAmount] = useState('');

  const connectMetaMask = async () => {
    const provider = await detectEthereumProvider();

    if (provider) {
      const web3Instance = new Web3(Web3.givenProvider || 'ws://localhost:8545');
      setWeb3(web3Instance);

      const accounts = await provider.request({ method: 'eth_requestAccounts' });
      setAccount(accounts[0]);

      const nftCheque = new web3Instance.eth.Contract(nftChequeAbi, nftChequeAddress);

      setNftChequeContract(nftCheque);
    } else {
      alert('Por favor, instala MetaMask para continuar');
    }
  };

  const emitCheque = async (e) => {
    e.preventDefault();
    if (!nftChequeContract || !web3) {
      alert('Por favor, conecta a MetaMask primero');
      return;
    }
    const weiAmount = web3.utils.toWei(amount, 'ether');
    try {
        await nftChequeContract.methods.mint(recipient, weiAmount).send({ from: account });
        alert('Cheque emitido con éxito');
    } catch (error) {
        alert('Error al emitir el cheque');
        console.log(error)
    }
  };

  useEffect(() => {
    connectMetaMask();
  }, []);

  return (
    <div className="ChequeEmitter">
      <form className="form" onSubmit={emitCheque}>
        <h2>Emitir Cheque</h2>
        <input
          className="input"
          type="text"
          placeholder="Dirección del receptor"
          value={recipient}
          onChange={(e) => setRecipient(e.target.value)}
        />
        <input
          className="input"
          type="number"
          placeholder="Cantidad"
          value={amount}
          onChange={(e) => setAmount(e.target.value)}
        />
        <button className="button" type="submit">
          Emitir Cheque
        </button>
      </form>
    </div>
  );  
};

export default ChequeEmitter;